col_input1, col_input2 = st.columns(2)

with col_input1:
    capital = st.number_input("أدخل رأس المال المتاح للاستثمار (بالريال السعودي):", min_value=100000, max_value=50000000, value=1200000, step=50000)

with col_input2:
    goal = st.selectbox("اختر الهدف الاستثماري الرئيسي لديك:", ["عوائد إيجارية سريعة ومستقرة (Yield)", "نمو رأس المال والمبيعات المستقبلية (Capital Growth)"])

# منطق تحليل الاستثمار العقاري الذكي بالرياض
st.write("#### 🎯 التوصية الاستثمارية المخصصة لك:")
with st.container(border=True):
    if capital < 800000:
        if goal == "عوائد إيجارية سريعة ومستقرة (Yield)":
            st.success("📍 **الحي المقترح:** قرطبة أو المونسية (شمال شرق الرياض)")
            st.markdown("""
            * **الفرصة الاستثمارية:** شقة سكنية (غرفتين وصالة) قريبة من المترو ومجمعات الأعمال.
            * **العائد المتوقع:** **8.2% إلى 9.0%** سنوياً.
            * **الخطوة القادمة:** البحث عن مطورين يقدمون ضمانات صيانة وإدارة أملاك مجانية لأول سنتين لتوفير التكاليف التشغيلية.
            """)
        else:
            st.success("📍 **الحي المقترح:** العارض أو الأمانة (شمال الرياض)")
            st.markdown("""
            * **الفرصة الاستثمارية:** شقة سكنية تحت الإنشاء (Off-plan) من مطور ذو موثوقية عالية للاستفادة من فروقات أسعار البيع بعد الجاهزية.
            * **النمو الرأسمالي المتوقع:** **10% إلى 12%** سنوياً بدعم من توسعات ممر التنمية الشمالي.
            """)
            
    elif 800000 <= capital < 2500000:
        if goal == "عوائد إيجارية سريعة ومستقرة (Yield)":
            st.success("📍 **الحي المقترح:** الملقا أو الصحافة (محيط المركز المالي KAFD)")
            st.markdown("""
            * **الفرصة الاستثمارية:** شقة فاخرة مخدومة أو شقة ذكية مؤثثة بالكامل لاستهداف شريحة الموظفين التنفيذيين والشركات العالمية.
            * **العائد المتوقع:** **7.8% إلى 8.5%** سنوياً مع معدل إشغال مرتفع يتجاوز 90%.
            """)
        else:
            st.success("📍 **الحي المقترح:** النرجس أو الياسمين (محيط المربع الجديد)")
            st.markdown("""
            * **الفرصة الاستثمارية:** تاون هاوس مودرن أو أرض سكنية صغيرة في مربع سكني مكتمل الخدمات. القرب من وجهة 'المربع الجديد الأيقونية' يضمن تضاعف الطلب.
            * **النمو الرأسمالي المتوقع:** **12% إلى 15%** سنوياً.
            """)
            
    else: # أكبر من 2.5 مليون
        if goal == "عوائد إيجارية سريعة ومستقرة (Yield)":
            st.success("📍 **الحي المقترح:** السليمانية أو المحمدية (وسط الرياض الحيوي)")
            st.markdown("""
            * **الفرصة الاستثمارية:** شراء مبنى تجاري/إداري صغير أو مجمع شقق سكنية متكامل وتأجيره بالكامل بعقد طويل الأجل لشركة أو مشغل واحد.
            * **العائد المتوقع:** **8.5% إلى 9.5%** عائد صافي خالي من المتاعب التشغيلية اليومية.
            """)
        else:
            st.success("📍 **الحي المقترح:** حطين أو ملقا الفاخرة (شمال غرب الرياض)")
            st.markdown("""
            * **الفرصة الاستثمارية:** شراء أراضٍ تجارية على شوارع رئيسية أو فلل سكنية فاخرة جداً ذات طابع عصري نادر (Premium Properties).
            * **النمو الرأسمالي المتوقع:** **15% إلى 18%** سنوياً نتيجة الندرة الشديدة للأراضي الفاخرة في هذه المربعات الذهبية.
            """)

